package BasicsOfJava;

public class doubledatatype {
    public static void main(String[] args) {
        double x = 5;
        double y = 2;
        double sum = x/y;
        System.out.println(sum);
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.print(x*y);
    }
}
