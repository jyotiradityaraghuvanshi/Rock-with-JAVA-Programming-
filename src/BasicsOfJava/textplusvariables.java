package BasicsOfJava;

public class textplusvariables {
    public static void main(String[] args) {
        int x = 5,y = 8,z = 4;
        System.out.println("HELLO" +x+y+z);
        System.out.println(x+y+z+"HELLO");
        System.out.println(x+"HELLO"+y+z);
        System.out.println(x+y+"HELLO"+z);
        System.out.println(x+z+"HELLO"+y);
    }
}
